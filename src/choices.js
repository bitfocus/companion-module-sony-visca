import { MODELS } from './models.js'

export function getChoices(config, self) {
	const model = MODELS.filter((model) => model.id === config.model)[0]
	const group = model.group
	const c = CHOICES
	c.IRIS = _choices[group].IRIS
	c.GAIN = _choices[group].GAIN
	if (Object.prototype.hasOwnProperty.call(_choices[group], 'GAIN_LIMIT')) {
		c.GAIN_LIMIT = _choices[group].GAIN_LIMIT
	} else {
		self.log('info', `GAIN_LIMIT is not a feature of the ${model.label} related actions will not be available.`)
	}
	if (Object.prototype.hasOwnProperty.call(_choices[group], config.frameRate)) {
		c.SHUTTER = _choices[group][config.frameRate].SHUTTER
		if (Object.prototype.hasOwnProperty.call(_choices[group][config.frameRate], 'MAX_SHUTTER')) {
			c.MAX_SHUTTER = _choices[group][config.frameRate].MAX_SHUTTER
		} else {
			self.log('info', `MAX_SHUTTER is not a feature of the ${model.label} related actions will not be available.`)
		}
	} else {
		self.log(
			'error',
			`${config.frameRate} is not a valid frame rate group for the ${model.label} providing choices for 50Hz based frame rates`
		)
		c.SHUTTER = _choices[group]['50'].SHUTTER
	}
	if (Object.prototype.hasOwnProperty.call(_choices[group], 'BRIGHTNESS')) {
		c.BRIGHTNESS = _choices[group].BRIGHTNESS
	} else {
		self.log('info', `BRIGHTNESS is not a feature of the ${model.label} related actions will not be available.`)
	}
	return c
}

